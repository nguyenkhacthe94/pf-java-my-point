public class MyPoint {
    private double x = 0, y = 0;

    public MyPoint() {
    }

    public MyPoint(double x, double y) {
    }

    public void setX(double x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
